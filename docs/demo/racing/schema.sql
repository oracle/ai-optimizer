-------------------------------------------------------------------------------
-- TABLES
-------------------------------------------------------------------------------
CREATE TABLE teams (
  team_id           NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  team_name         VARCHAR2(100) NOT NULL,
  engineering_focus VARCHAR2(100) NOT NULL,
  garage_location   VARCHAR2(100) NOT NULL
);

CREATE TABLE drivers (
  driver_id         NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  driver_code       VARCHAR2(20) NOT NULL UNIQUE,
  driver_label      VARCHAR2(20) NOT NULL UNIQUE,
  driver_group      VARCHAR2(40) NOT NULL,
  simulator_rig     VARCHAR2(40) NOT NULL,
  vehicle_setup     VARCHAR2(80) NOT NULL,
  team_id           NUMBER NOT NULL,
  driving_style     VARCHAR2(80) NOT NULL,
  skill_profile     VARCHAR2(200) NOT NULL,
  CONSTRAINT fk_drivers_team FOREIGN KEY (team_id) REFERENCES teams(team_id)
);

CREATE TABLE races (
  race_id           NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  race_name         VARCHAR2(100) NOT NULL,
  circuit           VARCHAR2(100) NOT NULL,
  country           VARCHAR2(80) NOT NULL,
  race_date         DATE NOT NULL,
  weather_condition VARCHAR2(80) NOT NULL
);

CREATE TABLE race_results (
  result_id         NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  race_id           NUMBER NOT NULL,
  driver_id         NUMBER NOT NULL,
  qualifying_position NUMBER(3) NOT NULL,
  finish_position   NUMBER(3),
  points            NUMBER(5,2) DEFAULT 0 NOT NULL,
  fastest_lap       NUMBER(10,3),
  status            VARCHAR2(40) NOT NULL,
  penalties         VARCHAR2(200),
  CONSTRAINT fk_results_race FOREIGN KEY (race_id) REFERENCES races(race_id),
  CONSTRAINT fk_results_driver FOREIGN KEY (driver_id) REFERENCES drivers(driver_id)
);

CREATE TABLE pit_stops (
  pit_stop_id       NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  race_id           NUMBER NOT NULL,
  driver_id         NUMBER NOT NULL,
  lap_number        NUMBER(4) NOT NULL,
  stop_duration_ms  NUMBER(8) NOT NULL,
  tyre_compound     VARCHAR2(20) NOT NULL,
  reason            VARCHAR2(120) NOT NULL,
  CONSTRAINT fk_pit_race FOREIGN KEY (race_id) REFERENCES races(race_id),
  CONSTRAINT fk_pit_driver FOREIGN KEY (driver_id) REFERENCES drivers(driver_id)
);

CREATE TABLE incidents (
  incident_id       NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  race_id           NUMBER NOT NULL,
  driver_id         NUMBER NOT NULL,
  incident_type     VARCHAR2(60) NOT NULL,
  severity          VARCHAR2(20) NOT NULL,
  lap_number        NUMBER(4) NOT NULL,
  short_description VARCHAR2(400) NOT NULL,
  CONSTRAINT fk_inc_race FOREIGN KEY (race_id) REFERENCES races(race_id),
  CONSTRAINT fk_inc_driver FOREIGN KEY (driver_id) REFERENCES drivers(driver_id)
);

CREATE TABLE performance_metrics (
  metric_id         NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  driver_id         NUMBER NOT NULL,
  race_id           NUMBER NOT NULL,
  avg_lap_time      NUMBER(10,3) NOT NULL,
  top_speed         NUMBER(6,2) NOT NULL,
  tyre_degradation   NUMBER(5,2) NOT NULL,
  overtakes         NUMBER(3) NOT NULL,
  mistakes          NUMBER(3) NOT NULL,
  CONSTRAINT fk_perf_driver FOREIGN KEY (driver_id) REFERENCES drivers(driver_id),
  CONSTRAINT fk_perf_race FOREIGN KEY (race_id) REFERENCES races(race_id)
);

CREATE TABLE source_documents (
  document_id       NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  document_type     VARCHAR2(40) NOT NULL,
  title             VARCHAR2(200) NOT NULL,
  race_id           NUMBER,
  driver_id         NUMBER,
  team_id           NUMBER,
  document_date     DATE NOT NULL,
  content           CLOB NOT NULL,
  CONSTRAINT fk_doc_race FOREIGN KEY (race_id) REFERENCES races(race_id),
  CONSTRAINT fk_doc_driver FOREIGN KEY (driver_id) REFERENCES drivers(driver_id),
  CONSTRAINT fk_doc_team FOREIGN KEY (team_id) REFERENCES teams(team_id)
);


