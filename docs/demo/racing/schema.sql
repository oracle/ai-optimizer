-- PRE-FINALE SYNTHETIC EXTENSION
-- Preserves the curated rows above and fills missing driver/race combinations
-- for Rounds 1-5. Round 6 stays scheduled but has no structured results so
-- the final championship answer requires the late-arriving RAG document.
-------------------------------------------------------------------------------
INSERT INTO race_results (
  race_id,
  driver_id,
  qualifying_position,
  finish_position,
  points,
  fastest_lap,
  status,
  penalties
)
WITH team_form AS (
  -- Per-team strength multiplier, computed once per reset. Drivers on a
  -- high-form team get smaller ORDER BY values below and so tend to qualify
  -- and finish higher across all 5 pre-finale rounds. Range 0.5..1.5 lifts
  -- the spread between strongest and weakest pre-finale team to ~80 pts,
  -- enough that any team can plausibly win the championship combined with
  -- the Round 6 bulletin.
  SELECT /*+ MATERIALIZE */
    team_id,
    0.5 + DBMS_RANDOM.VALUE AS form_factor
  FROM teams
),
candidates AS (
  SELECT /*+ MATERIALIZE */
    r.race_id,
    r.weather_condition,
    d.driver_id,
    d.driver_code,
    existing.existing_results,
    ROW_NUMBER() OVER (
      PARTITION BY r.race_id
      ORDER BY DBMS_RANDOM.VALUE / tf.form_factor, d.driver_id
    ) AS qualifying_rank,
    ROW_NUMBER() OVER (
      PARTITION BY r.race_id
      ORDER BY DBMS_RANDOM.VALUE / tf.form_factor, d.driver_id
    ) AS finish_rank,
    CASE r.race_id
      WHEN 1 THEN 92.000
      WHEN 2 THEN 89.500
      WHEN 3 THEN 84.800
      WHEN 4 THEN 87.300
      WHEN 5 THEN 91.700
      ELSE 96.100
    END AS base_lap
  FROM races r
  CROSS JOIN drivers d
  JOIN team_form tf ON tf.team_id = d.team_id
  JOIN (
    SELECT r2.race_id, COUNT(rr.result_id) AS existing_results
    FROM races r2
    LEFT JOIN race_results rr ON rr.race_id = r2.race_id
    GROUP BY r2.race_id
  ) existing ON existing.race_id = r.race_id
  LEFT JOIN race_results already_seeded
    ON already_seeded.race_id = r.race_id
   AND already_seeded.driver_id = d.driver_id
  WHERE already_seeded.result_id IS NULL
    AND r.race_id < 6
),
scored AS (
  -- MATERIALIZE so incident_score / lap_offset are evaluated once per row;
  -- otherwise the inlined CTE recomputes DBMS_RANDOM at each CASE branch
  -- and the status / penalties columns can disagree on the same row.
  SELECT /*+ MATERIALIZE */
    race_id,
    driver_id,
    existing_results + qualifying_rank AS qualifying_position,
    existing_results + finish_rank AS finish_position,
    base_lap,
    TRUNC(DBMS_RANDOM.VALUE(0, 100)) AS incident_score,
    TRUNC(DBMS_RANDOM.VALUE(0, 1500)) AS lap_offset
  FROM candidates
)
SELECT
  race_id,
  driver_id,
  qualifying_position,
  CASE WHEN incident_score < 4 THEN NULL ELSE finish_position END AS finish_position,
  CASE
    WHEN incident_score < 4 THEN 0
    WHEN incident_score < 11 THEN GREATEST(
      CASE finish_position
        WHEN 1 THEN 25
        WHEN 2 THEN 18
        WHEN 3 THEN 15
        WHEN 4 THEN 12
        WHEN 5 THEN 10
        WHEN 6 THEN 8
        WHEN 7 THEN 6
        WHEN 8 THEN 4
        WHEN 9 THEN 2
        WHEN 10 THEN 1
        ELSE 0
      END - 2,
      0
    )
    ELSE
      CASE finish_position
        WHEN 1 THEN 25
        WHEN 2 THEN 18
        WHEN 3 THEN 15
        WHEN 4 THEN 12
        WHEN 5 THEN 10
        WHEN 6 THEN 8
        WHEN 7 THEN 6
        WHEN 8 THEN 4
        WHEN 9 THEN 2
        WHEN 10 THEN 1
        ELSE 0
      END
  END AS points,
  ROUND(base_lap + (finish_position * 0.085) + ((lap_offset - 600) / 1000), 3) AS fastest_lap,
  CASE WHEN incident_score < 4 THEN 'DNF' ELSE 'Finished' END AS status,
  CASE
    WHEN incident_score < 4 THEN 'Mechanical retirement'
    WHEN incident_score < 11 THEN '5s penalty for track limits'
    ELSE NULL
  END AS penalties
FROM scored;

