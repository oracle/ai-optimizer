CREATE OR REPLACE VIEW driver_standings AS
SELECT
  d.driver_id,
  d.driver_code,
  d.driver_label,
  d.driver_group,
  d.simulator_rig,
  d.vehicle_setup,
  d.driving_style,
  t.team_name,
  SUM(rr.points) AS total_points,
  COUNT(CASE WHEN rr.status = 'Finished' THEN 1 END) AS finishes,
  COUNT(CASE WHEN rr.status = 'DNF' THEN 1 END) AS dnfs,
  MIN(rr.finish_position) AS best_finish,
  ROUND(AVG(pm.avg_lap_time), 3) AS avg_lap_time,
  ROUND(AVG(pm.top_speed), 2) AS avg_top_speed
FROM drivers d
JOIN teams t ON t.team_id = d.team_id
LEFT JOIN race_results rr ON rr.driver_id = d.driver_id
LEFT JOIN performance_metrics pm
  ON pm.driver_id = rr.driver_id
 AND pm.race_id = rr.race_id
GROUP BY
  d.driver_id,
  d.driver_code,
  d.driver_label,
  d.driver_group,
  d.simulator_rig,
  d.vehicle_setup,
  d.driving_style,
  t.team_name;

CREATE OR REPLACE VIEW team_standings AS
SELECT
  t.team_id,
  t.team_name,
  t.engineering_focus,
  COUNT(DISTINCT d.driver_id) AS drivers,
  SUM(rr.points) AS total_points,
  ROUND(AVG(pm.top_speed), 2) AS avg_top_speed,
  ROUND(AVG(pm.tyre_degradation), 2) AS avg_tyre_degradation,
  COUNT(i.incident_id) AS incident_count
FROM teams t
JOIN drivers d ON d.team_id = t.team_id
LEFT JOIN race_results rr ON rr.driver_id = d.driver_id
LEFT JOIN performance_metrics pm
  ON pm.driver_id = rr.driver_id
 AND pm.race_id = rr.race_id
LEFT JOIN incidents i
  ON i.driver_id = d.driver_id
 AND i.race_id = rr.race_id
GROUP BY
  t.team_id,
  t.team_name,
  t.engineering_focus;

CREATE OR REPLACE VIEW race_summary AS
SELECT
  r.race_id,
  r.race_name,
  r.circuit,
  r.race_date,
  r.weather_condition,
  COUNT(rr.result_id) AS classified_entries,
  COUNT(CASE WHEN rr.status = 'DNF' THEN 1 END) AS retirements,
  ROUND(AVG(pm.avg_lap_time), 3) AS field_avg_lap_time,
  ROUND(AVG(ps.stop_duration_ms), 0) AS avg_pit_stop_ms,
  COUNT(DISTINCT i.incident_id) AS incident_count
FROM races r
LEFT JOIN race_results rr ON rr.race_id = r.race_id
LEFT JOIN performance_metrics pm
  ON pm.race_id = rr.race_id
 AND pm.driver_id = rr.driver_id
LEFT JOIN pit_stops ps
  ON ps.race_id = rr.race_id
 AND ps.driver_id = rr.driver_id
LEFT JOIN incidents i
  ON i.race_id = rr.race_id
 AND i.driver_id = rr.driver_id
GROUP BY
  r.race_id,
  r.race_name,
  r.circuit,
  r.race_date,
  r.weather_condition;

COMMIT;